package com.someimportantprogram;

public class Swapping {
 public static void main(String[] args) {
   
     int a=10,b=20,c;
     System.out.println("Before swapping a and b\n"+a+" "+b);
     
     c=a;
     a=b;
     b=c;
     
     System.out.println("After swapping a and b\n"+a+" "+b);
     
   }
   
}
